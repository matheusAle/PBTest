    private void trocarDePainel(Painel p){
        if (painelAtivo != null){
            painelAtivo.preProcessamentoAntesDeFechar();
            conteudoContainer.remove(painelAtivo.getCompomenteRaiz());    
        }
        painelAtivo = p;
        conteudoContainer.setViewportView(p.getCompomenteRaiz());
        this.revalidate();
        this.repaint();
    }

    
