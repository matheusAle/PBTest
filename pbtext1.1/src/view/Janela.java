    private void configurarBotoesDeContexto(){
        switch (painelAtivo.pegarTipoDePainel()){
            case LISTAGEM:
                btnNovo.setVisible(true);
                btnSalvar.setVisible(false);
                btnCancelar.setVisible(false);
                btnLimpar.setVisible(false);
                campoDeBuscaContainer.setVisible(false);
                break;
            case FORMULARIO_DE_CRIACAO:
                btnNovo.setVisible(false);
                btnSalvar.setVisible(true);
                btnCancelar.setVisible(true);
                btnLimpar.setVisible(true);
                campoDeBuscaContainer.setVisible(false);
                break;
            case FORMULARIO_DE_EDICAO:
                btnNovo.setVisible(false);
                btnSalvar.setVisible(true);
                btnCancelar.setVisible(true);
                btnLimpar.setVisible(false);
                campoDeBuscaContainer.setVisible(false);
                break;
            case NULO:
                btnNovo.setVisible(false);
                btnSalvar.setVisible(false);
                btnCancelar.setVisible(false);
                btnLimpar.setVisible(false);
                campoDeBuscaContainer.setVisible(false);
                break;
            default:
                throw new AssertionError(painelAtivo.pegarTipoDePainel().name());
            
        }
    }
    
    private class BotoesDeNavegacaolistaner implements MouseListener {
        
        private Consumer<MouseEvent> onClick;
        private JButton content;
        public BotoesDeNavegacaolistaner(Consumer<MouseEvent> onClick, JButton btn) {
            this.onClick = onClick;
            content = btn;
        }
        @Override
        public void mouseClicked(MouseEvent e) {
            onClick.accept(e);   
        }
        @Override
        public void mousePressed(MouseEvent e) {
        }
        @Override
        public void mouseReleased(MouseEvent e) {
            content.setBackground(new java.awt.Color(204, 204, 204));
        }
        @Override
        public void mouseEntered(MouseEvent e) {
            content.setForeground(Color.white);
        }
        @Override
        public void mouseExited(MouseEvent e) {
            content.setBackground(new java.awt.Color(102, 102, 102));
            content.setForeground(new java.awt.Color(204, 204, 204));
        }
    }

